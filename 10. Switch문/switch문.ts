var numberdata = 20;

switch (numberdata) {

    case 50:

        console.log("numberdata의 값은 50입니다.");
        break;

    case 40:

        console.log("numberdata의 값은 40입니다.");
        break;

    case 30:

        console.log("numberdata의 값은 30입니다.");
        break;

    case 20:

        console.log("numberdata의 값은 20입니다.");
        break;

    case 10:

        console.log("numberdata의 값은 10입니다.");
        break;

    case 0:

        console.log("numberdata의 값은 0입니다.");
        break;

    default:

        console.log("값이 case 범위내에 존재하지 않습니다.");
        break;

}